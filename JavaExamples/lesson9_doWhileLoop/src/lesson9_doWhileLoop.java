public class lesson9_doWhileLoop {
    public static void main(String[] args){
        //Do-While Loop
        int j=4;
        do{
            System.out.println("Logged."); //Logging the code blocks.
            System.out.println(j);
            j+=2;
        }
        while(j<10);
        System.out.println("End of the Do-While Loop.");
    }
}
