public class lesson4_ifElseBlocks {
    public static void main(String[] args){
        int num1 = 24;
        if(num1 < 20){
            System.out.println("Number is less than 20");
        }
        else if(num1 == 20){
            System.out.println("Number is equal to 20");
        }
        else{
            System.out.println("Number is higher than 20");
        }
    }
}
